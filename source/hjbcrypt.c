void hjb_encrypt_block(unsigned char out[8], unsigned char in[8], unsigned char key[8])
{
	size_t i, j, k;
	uint64_t val;

	for (i = 0; i < 8; ++i) {
#ifdef HARDENED
		/* First stage. */
		val = Te0[(key[i] & 0xf0) ^ in[i]];

		for (j = 0; j < 8; ++j) {
			k = (i + j) % 8;
			out[i] ^= val & 0xff;
			val >>= 8;
		}

		/* Second stage. */
		val = Te1[((key[i] << 4) & 0xf0) ^ in[i]];

		for (j = 0; j < 8; ++j) {
			k = (i + j) % 8;
			out[i] ^= val & 0xff;
			val >>= 8;
		}
#else
		val = Te0[(key[i] ^ in[i]) << 3];

		for (j = 0; j < 8; ++j) {
			k = (i + j) % 8;
			out[i] ^= val & 0xff;
			val >>= 8;
		}
#endif
	}
}

int hjb_encrypt(unsigned char *out, unsigned char *in, unsigned char key[8],
	size_t len)
{
	size_t i;

	/* This block cipher expects the length of the input to be a multiple of
	 * 8.
	 */
	if ((len % 8) != 0)
		return -1;

	/* Clear the output. */
	memset(out, 0, len);

	/* Encrypt the blocks. */
	for (i = 0; i < len; i += 8) {
		hjb_encrypt_block(out + i, in + i, key);
	}

	return 0;
}

